    private void montarTabela() {
        
        try {
            ArrayList<ContaCorrente> contas = contaDao.listarContaCorrente();
            
            int linha = 0;
            for(ContaCorrente conta : contas){
                int id = conta.getIdConta();
                int numConta = conta.getNumConta();
                double saldo = conta.getSaldo();
                int senha = conta.getSenha();
                int pessoa = conta.getCliente();
                
                tblCC.getModel().setValueAt(id, linha, 0);
                tblCC.getModel().setValueAt(numConta, linha, 1);
                tblCC.getModel().setValueAt(pessoa, linha, 2);
                tblCC.getModel().setValueAt(saldo, linha, 3);
                tblCC.getModel().setValueAt(senha, linha, 4);
                linha++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ListarContaC.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    
    





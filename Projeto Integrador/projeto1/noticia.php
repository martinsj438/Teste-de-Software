					<h1>Petrobras anuncia nova redução no preço da gasolina; queda no mês chega a 19%</h1>
					<h3>Litro do combustível nas refinarias passará de R$ 1,5556 para R$ 1,5007, o que representa uma redução de 3,53%.</h3>
					<div class="col-sm-8">
						<img src="gasolina.jpg">
					</div>
					<p>A Petrobras anunciou uma nova redução no preço da gasolina vendida nas refinarias. A partir desta terça-feira (27), o litro do combustível passará de R$ 1,5556 para R$ 1,5007, o que representa uma redução de 3,53%.

					Com mais esse corte, o preço da gasolina nas refinarias já acumula no mês queda de 19,42%.

					O repasse dos reajustes da Petrobras nas refinarias aos consumidores depende dos distribuidores – ou seja, fica a cargo dos postos repassar ou não a baixa do preço da gasolina.</p>
					<p>A nova redução nas refinaris ocorre após os preços do petróleo terem despencado quase 8% na última sexta-feira (23), ao menor nível em mais de um ano, registrando a sétima perda semanal consecutiva em meio a receios de um excesso de oferta, apesar de nações produtoras considerarem cortes na produção.

					A Petrobras adota novo formato na política de ajuste de preços desde 3 de julho do ano passado. Pela nova metodologia, os reajustes acontecem com maior periodicidade, inclusive diariamente.</p>	
					<h1>Como é formado o preço da gasolina?</h1>
					<p>Os valores praticados pela Petrobras representam atualmente menos de um terço (27%) do preço pago pelo consumidor nos postos, conforme os cálculos da estatal, que levam em conta a coleta de preços entre os dias 11 e 17 de novembro em 13 regiões metropolitanas do país.</p>
					<p>Cerca de 45% são tributos, sendo 30% ICMS, recolhido pelos Estados, e 15% Cide e PIS/Cofins, de competência da União.

					Os tributos federais são cobrados como um valor fixo por litro - o de Pis/Cofins, por exemplo, é de R$ 0,7925 por litro de gasolina; a Cide, de R$ 0,10 por litro. O ICMS, por sua vez, é um percentual sobre o preço de venda - ou seja, cada vez que ele sobe, os Estados recolhem mais impostos.

					Do restante da composição do preço final, 12% é o custo do etanol, que, por lei, deve compor 27% da gasolina comum, e outros 16% corresponde aos custos e lucro dos distribuidores e postos de gasolina. Em maio, esta última fatia era de 12%, e no final de outubro era de 14%, o que sugere um aumento nas margens de lucro destes agentes.</P>
